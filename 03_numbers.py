# Numbers in Python Version 3

# types of numbers: int, float, complex

# integers
# no size limits or overflows for ints, as long as your computer can store it
a = 496 # this is a perfect number
print(type(a)) # the type function allows you to see the data type
print(a) # prints the value of a

# floats
e = 2.718281828
print(type(e))

# complex numbers
# has a real part and an imaginary part
# j = sqrt(-1)
z = 2 - 6.1j
print(type(z))
print(z.real) # prints the real part
print(z.imag) # prints the imaginary part
# Python stores the real and imaginary parts of complex numbers as floats